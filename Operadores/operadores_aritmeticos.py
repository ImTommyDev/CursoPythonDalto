suma = 10 + 5 # Suma
resta = 10 - 5 #Resta
multiplicacion = 10 * 5 # Multiplicación
division = 10 / 5 # La división siempre devuelve un float
modulo = 10 % 5 # Módulo (resto de la división)
potencia = 10 ** 5 #Exponente
division_entera = 10 // 5 # División entera. Devuelve entero redondeado hacia abajo (2.9 será 2)

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("Módulo:", modulo)
print("Potencia:", potencia)
print("División entera:", division_entera)