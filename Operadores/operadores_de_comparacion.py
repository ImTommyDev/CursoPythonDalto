#Operadores de comparación

es_igual = 10 == 10 # Compara si son iguales
es_diferente = 10 != 5 # Compara si son diferentes
es_mayor = 10 > 5 # Compara si el primer número es mayor que el segundo
es_menor = 10 < 5 # Compara si el primer número es menor que el segundo
es_mayor_igual = 10 >= 5 # Compara si el primer número es mayor o igual que el segundo
es_menor_igual = 10 <= 5 # Compara si el primer número es menor o igual que el segundo

#Muestra los resultados
print("Es igual:", es_igual)
print("Es diferente:", es_diferente)
print("Es mayor:", es_mayor)
print("Es menor:", es_menor)
print("Es mayor o igual:", es_mayor_igual)
print("Es menor o igual:", es_menor_igual)

